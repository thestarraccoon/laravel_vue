<template>
    <div class="">
        <div class="mb-3">
            <input type="text" class="form-control" v-model="name" id="_name">
        </div>
        <div class="mb-3">
            <input type="text" class="form-control" v-model="age" id="age">
        </div>
        <div class="mb-3">
            <input type="text" class="form-control" v-model="job" id="job">
        </div>
        <div class="mb-3">
            <input @click.prevent="addPerson" class="btn btn-primary" value="Добавить">
        </div>
    </div>
</template>

<script>
    export default {
        name: "CreateComponent",

        data() {
            return {
                name: null,
                age: null,
                job: null
            }
        },

        methods: {
            addPerson() {
                axios.post('/api/people', {
                    name: this.name,
                    age: this.age,
                    job: this.job
                })
                .then(res => {
                    this.name = null,
                    this.age = null,
                    this.job = null
                })
            }
        }
    }

</script>

